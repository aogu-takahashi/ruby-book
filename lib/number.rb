# 期数指数子を用いた整数リテラル
p "2進数"
p 0b11111111

p "8進数"
p 0o377

p "16進数"
p 0xff

p "10進数"
p 0d255

# ビット演算
# メモリを効率的につかえる
# 計算が高速に行える
# 状態管理を効率化できる

p "指数表現"
p 2e-3

# 数値クラスのあれこれ
p "クラス"
p 10.class
p 1.5.class

# 有理数リテラル
r = 2 / 3r
p r
p r.class

p "文字列から有理数に変換"
r = "2/3".to_r
p r
p r.class

p "有理数から文字列に変換"
p r.to_s

p "複素数リテラル"
c = 0.3 + 0.5i
p c
p c.class

p "文字列から複素数に変換"
c = "0.3+0.5i".to_c
p c
p c.class
